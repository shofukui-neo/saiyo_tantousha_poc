// 名（given name）として許容する字種（漢字・ひらがな・カタカナ・長音）。異体字は正規化で標準化される。
const GIVEN_OK = /^[一-龥々぀-ゟ゠-ヿー]{1,3}$/;

/**
 * 氏名候補を「姓＋名」に分解する。先頭から長い姓（3→2→1字）を優先照合。
 * @param {string} raw 例: "山田太郎" / "山田 太郎" / "山田"
 * @returns {{sei:string, mei:string}|null} 既知姓で始まらなければ null
 */
function splitName(raw) {
  // 異体字を標準化してから辞書照合（髙橋→高橋, 小松﨑→小松崎）。出力sei/meiも標準形に揃う。
  const s = normalizeNameKanji(String(raw || '').replace(/[ 　]/g, ''));
  if (!s) return null;
  for (const len of [3, 2, 1]) {
    const sei = s.slice(0, len);
    if (SURNAMES.has(sei)) return { sei, mei: s.slice(len) };
  }
  return null;
}

/**
 * 姓＋名がそろった「フルネームらしさ」。担当者名として確定に使う。
 * @param {string} raw
 * @returns {boolean}
 */
function isFullName(raw) {
  const sp = splitName(raw);
  return !!(sp && sp.mei && GIVEN_OK.test(sp.mei));
}

/**
 * 既知の姓で始まるか（名が省略された「人事の山田です」等を、強い文脈のときだけ許容する用途）。
 * @param {string} raw
 * @returns {boolean}
 */
function isKnownSurname(raw) {
  const sp = splitName(raw);
  return !!(sp && (sp.mei === '' || GIVEN_OK.test(sp.mei)));
}

// 氏名領域から剥がす非氏名スパン（役割語・役職・地名/拠点語）。氏名抽出の前処理で使う共通辞書。
// 例:「田中花子採用担当」→「田中花子」/「山田太郎部長」→「山田太郎」/「関東支店」→「」。
const ROLE_GLUE_RE = /採用責任者|人事責任者|採用ご?担当者?|人事ご?担当者?|ご担当者?|担当者?/g;
const TITLE_RE = /代表取締役|代表|副社長|社長|専務|常務|取締役|監査役|執行役員|執行役|副会長|会長|理事長|理事|本部長|部長|次長|課長|係長|室長|店長|所長|主任|主査|主事|参事|マネージャー|マネジャー|リーダー|チーフ|リクルーター/g;
const GEO_RE = /関東|関西|東海|東北|九州|北海道|沖縄|信越|北陸|中国|四国|本店|支店|支社|支局|営業所|事業所|事務所|出張所|本社|東京|大阪|京都|名古屋|横浜|神戸|福岡|札幌|仙台|広島|信用金庫|信用金|信用組合|信用組|農業協同組合|農協/g;
// 宛先・敬称的サフィックス（「池田宛」「○○御中」等）。氏名にこれらは含まれないため安全に剥がす。
// 注: 「行」は給名語尾(正行/和行 等)に頻出するため剥がさない。
const ADDR_RE = /御中|宛先|宛て|宛|様方|気付/g;

/** 氏名候補の文字列から、貼り付いた役割語・役職・地名・宛先語スパンを除去する。 */
function stripNonName(s) {
  return String(s || '').replace(ROLE_GLUE_RE, ' ').replace(TITLE_RE, ' ').replace(GEO_RE, ' ').replace(ADDR_RE, ' ');
}

/**
 * 語が辞書に「完全な姓」として載るか（中村/佐々木=その姓、地名片 関東/中央=''）。
 * 名を伴わない単独姓を、地名片と区別して安全に採用するための判定。村/市で終わる頻出姓も拾える。
 * @returns {string} 完全一致した姓（無ければ ''）
 */
function completeSurname(win) {
  const sp = splitName(win);
  const norm = normalizeNameKanji(String(win).replace(/[\s　]/g, ''));
  return (sp && sp.mei === '' && sp.sei === norm) ? sp.sei : '';
}

