// テーブルのキー（不正な実験用キーは除外）を長い順に並べておく（最長一致用）
const SORTED_CODES = Object.keys(AREA_CODES).filter(k => /^0\d+$/.test(k)).sort((a, b) => b.length - a.length);

// 10桁の固定電話digitsに最長一致する市外局番を返す（無ければ null）
function matchAreaCode(digits) {
  if (!/^\d{10}$/.test(digits)) return null;
  for (const code of SORTED_CODES) if (digits.startsWith(code)) return code;
  return null;
}

// digits(10桁) を市外局番テーブルに基づき 0AB-CDE-FGHJ 形式へ整形（不明なら null）
function formatLandline(digits) {
  const code = matchAreaCode(digits);
  if (!code) return null;
  const last4 = digits.slice(6);          // 加入者番号は常に末尾4桁
  const middle = digits.slice(code.length, 6);
  return middle ? `${code}-${middle}-${last4}` : `${code}-${last4}`;
}

// 番号（任意表記）から都道府県名を推定（市外局番テーブル）。固定電話のみ。
function prefectureForNumber(raw) {
  const digits = String(raw || '').replace(/[^\d]/g, '');
  if (digits.length !== 10) return '';
  const code = matchAreaCode(digits);
  return code ? AREA_CODES[code] : '';
}

